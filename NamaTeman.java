public class NamaTeman {
        

public static void main(String[] args) {
        String[] arrayNamaTeman = new String[5]; // Membuat array dengan 5 elemen

        // Mengisi array dengan nama teman
        arrayNamaTeman[0] = "Sarah";
        arrayNamaTeman[1] = "Angel";
        arrayNamaTeman[2] = "Wildan";
        arrayNamaTeman[3] = "Ridho";
        arrayNamaTeman[4] = "Farhan";

        // Menampilkan nama teman
        for (int i = 0; i < arrayNamaTeman.length; i++) {
            System.out.println(arrayNamaTeman[i]);
        }
    }
}
    
      

