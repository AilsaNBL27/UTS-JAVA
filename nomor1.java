public class nomor1 {
    public static void main(String[] args) {
        // Membuat array integer dengan ukuran 10
        int[] array = new int[10];

        // Menginisialisasi elemen-elemen array
        array[0] = 1;
        array[1] = 2;
        array[2] = 3;
        array[3] = 4;
        array[4] = 5;
        array[5] = 6;
        array[6] = 7;
        array[7] = 8;
        array[8] = 9;
        array[9] = 10;

        // Menampilkan elemen-elemen array
        for (int i = 0; i < array.length; i++) {
            System.out.println("Elemen ke-" + i + ": " + array[i]);
        }
    }
}


   